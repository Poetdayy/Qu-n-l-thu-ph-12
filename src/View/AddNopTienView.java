    public void backtoNopTienViewClick() {
        new NopTienView().setVisible(true);
        this.dispose();
    }

    public NopTienModel btnThemClick() throws ParseException {
        NopTienModel nt = new NopTienModel();
        nt.setNgayThu(formatDate.parse(txtInputDate.getText()));
       
        NhanKhauModel nk1 = nkDAO.getByName(cboTenNguoiNop.getSelectedItem().toString());
        nt.setTenNhanKhau(nk1);
        
        KhoanThuModel kt1 = ktDAO.getByName1(cboTenKhoanThu.getSelectedItem().toString());
        nt.setTenKhoanThu(kt1);
        return nt;
    }

