    // Đổ dữ liệu vào combo box
    public void getCBoxNhanKhau() {
        DefaultComboBoxModel cboModel = (DefaultComboBoxModel) cboTenNguoiNop.getModel();
        for (NhanKhauModel nk : nkDAO.getAll()) {
            cboModel.addElement(nk);
        }
    }

    public void getCBoxKhoanThu() {
        DefaultComboBoxModel cboModel = (DefaultComboBoxModel) cboTenKhoanThu.getModel();
        for (KhoanThuModel kt : ktDAO.getAllKhoanThu()) {
            cboModel.addElement(kt);
        }
    }

