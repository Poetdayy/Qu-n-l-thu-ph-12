    private void delHoKhaubtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delHoKhaubtnActionPerformed
        // TODO add your handling code here:
        if (strID.equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn hộ khẩu để xóa");
        } else {
            if (dao.delHoKhau(Integer.parseInt(strID)) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                fillTable();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }
    }//GEN-LAST:event_delHoKhaubtnActionPerformed

