    public void fillTable() {
        DefaultTableModel tbModel = (DefaultTableModel) TableHK.getModel();
        tbModel.setRowCount(0); // Xoa data trong table
        Object dataRow[] = new Object[5];
        for (HoKhauModel hk : dao.getAll()) {
          
            dataRow[0] = hk.getMaHo();
            dataRow[1] = hk.getIDChuHo();
            dataRow[2] = hk.getSoThanhvien();
            dataRow[3] = hk.getDiaChi();
            tbModel.addRow(dataRow); // add du lieu vao table Model
        }
      
        
       
     
    }

