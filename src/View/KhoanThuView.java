    public void fillTable()
    {
        DefaultTableModel tbModel = (DefaultTableModel) TableKT.getModel();
        tbModel.setRowCount(0); // Xoa data trong table
        for(KhoanThuModel kt : dao.findKhoanThuModelbyName(strFind))
        {
            Object dataRow[] = new Object[5];
            dataRow[0] = kt.getMaKhoanThu();
            dataRow[1] = kt.getTenKhoanThu();
            dataRow[2] = kt.getSoTien();
            dataRow[3] = kt.getLoaiKhoanThu();
            tbModel.addRow(dataRow); // add du lieu vao table Model
        }
    }
        
    
    

