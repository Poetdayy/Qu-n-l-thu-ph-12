    private void delKhoanThubtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delKhoanThubtnActionPerformed
        // TODO add your handling code here:
        if(strID.equals("")){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn sinh viên để xóa");
        }else{
            if(dao.delKhoanThu(Integer.parseInt(strID))>0)
            {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            fillTable();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }
    }//GEN-LAST:event_delKhoanThubtnActionPerformed

    private void editKhoanThubtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editKhoanThubtnActionPerformed
        // TODO add your handling code here:
        selectedIndex = TableKT.getSelectedRow();
        if(dao.getAllKhoanThu().size() == 0){
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập thêm khoản thu cần sửa" );
        }
        else if(selectedIndex  == -1){
            JOptionPane.showMessageDialog(rootPane,"Hãy chọn khoản thu cần sửa");
        }
        else{
            EditKhoanThuView edit = new EditKhoanThuView();
            edit.setEditData(dao.getAllKhoanThu().get(selectedIndex));
            edit.setVisible(true);
            this.dispose();
       }
        
    }//GEN-LAST:event_editKhoanThubtnActionPerformed

